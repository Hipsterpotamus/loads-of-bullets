function fillSet(color) {
    switch (color) {
        case "yellow":
            fill(250, 250, 50);
            break;
        case "red":
            fill(255, 80, 80);
            break;
        case "blue":
            fill(80, 80, 255);
            break;
        case "green":
            fill(80, 255, 80);
            break;
        case "orange":
            fill(255,128,50)
            break;
        case "teal":
            fill(80,250,250);
            break;
        case "pink":
            fill(255,100,150);
            break;
        case "light grey":
            fill(180);
            break;
        case "dark grey":
            fill(60);
            break;
    }
}